main proc

    cls
    
    invoke CreateFile,addr fileName,GENERIC_READ,NULL,NULL,OPEN_EXISTING,FILE_ATTRIBUTE_NORMAL,NULL
    mov hFile, eax

    invoke GetLastError
    print str$(eax), 13, 10

    invoke ReadFile,hFile,addr buffer,bcnt,ADDR var,NULL
    cmp		[var], 0
	   je		readingDone
readingDone:
    invoke GetLastError
    print str$(eax), 13, 10

    print addr buffer, 13, 10

    ret

main endp

